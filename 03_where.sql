-- WHERE
-- Useful whenever we want to return filtered data:
SELECT column1,column2,column_n
FROM someTable
WHERE condition

/*
Operetors are used to give conditions:

OPERATOR   -   DESCRIPTION
=               Equal to
>               Greater than
<               Less than
>=              Greater than or equal to
<=              Less than or equal to
<>              Different than
AND             And
OR              Or

*/
